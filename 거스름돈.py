N = int(input())
N = 1000 - N
cnt = 0
while N != 0:
  if N - 500 >= 0:
    N -= 500
    cnt += 1
  elif N - 100 >= 0:
    N -= 100
    cnt += 1
  elif N - 50 >= 0:
    N -= 50
    cnt += 1
  elif N - 10 >= 0:
    N -= 10
    cnt += 1
  elif N - 5 >= 0:
    N -= 5
    cnt += 1
  elif N - 1 >= 0:
    N -= 1
    cnt += 1
print(cnt)